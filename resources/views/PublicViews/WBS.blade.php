    <h2>PANDUAN LAPORAN</h2>
    <p>Penulisan panduan dilakukan dengan langkah sebagai berikut :</p>
    <p>1. Klik tombol “Login”, lalu isikan Username dan Password anda.</p>
    <p>
      2. Jika anda belum terdaftar, maka klik tombol “Register” dan isikan
      data diri anda, kemudian klik rombol “Register”, maka anda akan otomatis
      login ke aplikasi.
    </p>
    <p>3. Klik menu “Pengaduan” untuk merekam pengaduan baru.</p>
    <p>
      4. Klik tombol “Tambah Pengaduan” untuk menambahkan ke pengaduan baru.
    </p>
    <p>
      5. Isi form Tambah Pengaduan sesuai informasi yang anda ketahui, lalu
      klik tombol “Lanjut”.
    </p>
    <p>6. Perhatikan baik - baik beberapa hal berikut ini :</p>
    <p>- Semua kotak tang diberi tanda (*) wajib diisi.</p>
    <p>
      - Pastikan informasi yang diberikan sedapat mungkin memenuhi unsur 4W +
      1H.
    </p>
    <p>
      7. Jika anda memiliki bukti dalam bentuk file seperti foto atau dokumen
      lain, silahkan dilengkapi pada halaman pengaduan dengan cara :
    </p>
    <p>
      - Setelah membaca petunjuk untuk menyertakkan lampiran , klik kotak
      kecil dibawah petunjuk tersebut dan lanjutkan prosesnya.
    </p>
    <p>
      8. Setelah selesai mengisi, silahkan klik tombil “Daftar” untuk
      melanjutkan atau klik tombol “Hapus” untuk membatalkan pelaporan anda.
    </p>
    <p>
      9. Halaman berikutnya memberi kesempatan bagi anda yang ingin mencetak
      nomor register pengaduan.
